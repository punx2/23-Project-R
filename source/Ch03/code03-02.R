a <- 10                          # 변수 a에 10을 저장
b <- 20                          # 변수 b에 20을 저장
c <- a + b                       # 변수 a, b에 저장된 값을 더하여 변수 c에 저장
print(a)                         # 변수 a의 내용 출력
print(b)                         # 변수 b의 내용 출력
print(c)                         # 변수 c의 내용 출력